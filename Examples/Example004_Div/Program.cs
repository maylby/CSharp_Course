// Деление чисел
// "int" (integer) - контейнер хранения целых чисел
int numberA = 12;
int numberB = 5;
int result = numberA / numberB;
Console.WriteLine(result);
// При делении целых чисел ("int"), остаток отделения отбрасывается
// В данном случае, оставлена, только целая часть (2), дробная отброшены (0,4)

// "double" - контейнер хранения дробей
double numberC = 12;
double numberD = 5;
double result2 = numberC / numberD;
Console.WriteLine(result2);
